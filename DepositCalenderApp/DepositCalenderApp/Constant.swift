import UIKit

extension UIColor {
    class func lightBlue() -> UIColor {
        return UIColor(red: 92.0 / 255, green: 192.0 / 255, blue: 210.0 / 255, alpha: 1.0)
    }
    
    class func lightRed() -> UIColor {
        return UIColor(red: 195.0 / 255, green: 123.0 / 255, blue: 175.0 / 255, alpha: 1.0)
    }
    
    class func lightOrange() -> UIColor {
        return UIColor(red: 255.0 / 255.0, green: 128.0 / 255.0, blue: 0.0, alpha: 1.0)
    }
    
    class func offWhite() -> UIColor {
        return UIColor(red: 255.0 / 255.0, green: 252.0 / 255.0, blue: 237.0 / 255.0, alpha: 1.0)
    }
    
    class func lightGreen() -> UIColor {
        return UIColor(red: 230.0 / 255.0, green: 246.0 / 255.0, blue: 243.0 / 255.0, alpha: 1.0)
    }
    
    class func lightPink() -> UIColor {
        return UIColor(red: 255.0 / 255.0, green: 232.0 / 255.0, blue: 224.0 / 255.0, alpha: 1.0)
    }
    
    class func brown() -> UIColor {
        return UIColor(red: 139.0 / 255.0, green: 87.0 / 255.0, blue: 42.0 / 255.0, alpha: 1.0)
    }


}
